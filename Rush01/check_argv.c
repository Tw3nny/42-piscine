int	check_argv(char **argv)
{
	int	i;
	int	n;

	i = 0;
	n = 0;
	while (argv[1][i])
	{
		if (argv[1][i] == 32 || argv[1][i] == 9)
			i++;
		else if (argv[1][i] < 49 || argv[1][i] > 57)
			return (0);
		else
		{
			n++;
			i++;
		}
	}
	if (n % 4 != 0)
		return (0);
	else
		n /= 4;
	return (n);
}
